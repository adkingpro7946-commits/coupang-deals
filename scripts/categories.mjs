// 쿠팡이 상품마다 주는 카테고리명(categoryName)은 비슷한 게 여럿으로 갈린다
// (예: "스포츠/레저" vs "스포츠/레저용품"). 이 표로 상위 카테고리 하나에 묶는다.
//
// 규칙: 여기 없는 이름은 그대로 통과한다. 새 카테고리가 들어와도 사이트는 깨지지 않고,
//       나중에 이 표에 한 줄 추가하면 병합된다.
//
// 키(왼쪽)는 쿠팡 원본명, 값(오른쪽)은 화면에 보일 정리된 이름이다.
export const CATEGORY_MAP = {
  // 가전·디지털
  '가전디지털': '가전·디지털',

  // 주방
  '주방용품': '주방용품',

  // 홈·리빙 (인테리어/가구/생활용품 통합)
  '홈인테리어': '홈·리빙',
  '가구/홈인테리어': '홈·리빙',
  '생활용품': '홈·리빙',

  // 스포츠·레저
  '스포츠/레저': '스포츠·레저',
  '스포츠/레저용품': '스포츠·레저',

  // 패션 (의류/잡화 통합)
  '패션잡화': '패션·잡화',
  '여성패션': '패션·잡화',
  '남성패션': '패션·잡화',

  // 식품 (신선/일반 통합)
  '식품': '식품',
  '로켓프레시': '식품',

  // 헬스·건강
  '헬스/건강식품': '헬스·건강',

  // 반려동물
  '반려/애완용품': '반려동물',
  '반려동물용품': '반려동물',

  // 여행
  '국내투어': '여행',

  // 소수 카테고리는 "기타"로 묶어 파편화를 막는다
  '출산/유아': '기타',
  '완구/취미': '기타',
  '도서/음반': '기타',
  '자동차용품': '기타',
};

/** 원본 카테고리명 → 정리된 상위 카테고리. 매핑에 없으면 원본을 그대로 반환. */
export function canonicalCategory(raw) {
  if (!raw) return '기타';
  const trimmed = String(raw).trim();
  return CATEGORY_MAP[trimmed] || trimmed;
}
